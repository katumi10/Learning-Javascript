// Javascript string methods


let myName = "Katumi Amadu";
let phone = "023-4784-6156";

console.log(myName)

console.log(myName.length); //length of  a string
console.log(myName.charAt(2)) // returns the character at a specified index(position)
console.log(myName.indexOf("i"))// returns the index of the first occurence of a character
console.log(myName.lastIndexOf("a"))// last occurence of  a string
console.log(myName.toUpperCase()) 
console.log(myName.toLowerCase())

console.log(phone)
console.log(phone.replaceAll("-","/")) //replaces - with /

console.log(myName.concat(" ",phone)) //joins two strings together

console.log(myName.includes("y")) // returns true if a string contains a specific character

console.log(myName.slice(2,7)) // extracts a part of a string and returns the extracted part






